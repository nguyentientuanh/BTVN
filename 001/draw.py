import turtle 

# Bước 2: lay but viet
t = turtle.Turtle()

# Bước 3: Bat dau ve
# tien toi 100 don vi
t.forward(100)
#di chuyen xuong
t.right(90)
# di xuong 100 don vi
t.forward(100)
# di chuyen sang
t.right(90)
# di sang 100 don vi
t.forward(100)
# di chuyen len
t.right(90)
# di len 100 don vi
t.forward(100)
turtle.done
